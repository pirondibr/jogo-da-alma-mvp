    async streamAnalysis(situation, msgElement) {
        const contentEl = msgElement.querySelector('#aiContent');
        const loadingEl = msgElement.querySelector('#aiLoading');
        let fullText = '';

        try {
            const response = await fetch('https://openrouter.ai/api/v1/chat/completions', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'Authorization': `Bearer ${this.apiKey}`,
                    'HTTP-Referer': window.location.href,
                    'X-Title': 'Jogo da Alma'
                },
                body: JSON.stringify({
                    model: this.model,
                    messages: [
                        { role: 'system', content: SYSTEM_PROMPT },
                        { role: 'user', content: 'Use esta base de referência completa dos 7 chakras para suas análises. Ela contém TODAS as variáveis (necessidades, objetivos, situações, emoções, pensamentos, ações, competências, capacidades, ferramentas, resultados, missões e experiências formativas) para cada chakra, além da arquitetura do método e interações entre chakras:\n\n' + CHAKRA_REFERENCE },
                        { role: 'assistant', content: 'Entendido. Tenho acesso à base completa dos 7 chakras com todas as variáveis. Vou usar toda esta referência para gerar análises completas, precisas e personalizadas seguindo os 3 módulos (Espelho, Fórmula, Protocolo). Aguardo a situação para analisar.' },
                        { role: 'user', content: situation }
                    ],
                    stream: true,
                    max_tokens: 12000,
                    temperature: 0.7
                })
            });

            if (!response.ok) {
                const errData = await response.json().catch(() => ({}));
                throw new Error(errData.error?.message || `Erro ${response.status}: ${response.statusText}`);
            }

            this.stopLoadingMessages();
            loadingEl.style.display = 'none';
            contentEl.style.display = 'block';

            const reader = response.body.getReader();
            const decoder = new TextDecoder();
            let buffer = '';

            while (true) {
                const { done, value } = await reader.read();
                if (done) break;

                buffer += decoder.decode(value, { stream: true });
                const lines = buffer.split('\n');
                buffer = lines.pop() || '';

                for (const line of lines) {
                    const trimmed = line.trim();
                    if (!trimmed || !trimmed.startsWith('data: ')) continue;

                    const data = trimmed.slice(6);
                    if (data === '[DONE]') continue;

                    try {
                        const parsed = JSON.parse(data);
                        const delta = parsed.choices?.[0]?.delta?.content;
                        if (delta) {
                            fullText += delta;
                            contentEl.innerHTML = this.renderMarkdown(fullText);
                            this.scrollChat();
                        }
                    } catch (_) { /* skip malformed chunks */ }
                }
            }

            this.reportMarkdown = fullText;
            this.downloadArea.style.display = 'flex';
            this.scrollChat();

        } catch (err) {
            this.stopLoadingMessages();
            loadingEl.style.display = 'none';
            contentEl.style.display = 'block';
            contentEl.innerHTML = `<div class="error-msg">
                <strong>Erro ao gerar análise:</strong> ${this.escapeHtml(err.message)}
                <br><br>Verifique sua chave de API e tente novamente.
            </div>`;
        } finally {
            this.isGenerating = false;
            this.sendBtn.disabled = false;
            this.userInput.disabled = false;
        }
    }

    renderMarkdown(text) {
        if (typeof marked !== 'undefined') {
            return marked.parse(text);
        }
        return text
            .replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;')
            .replace(/^### (.+)$/gm, '<h3>$1</h3>')
            .replace(/^## (.+)$/gm, '<h2>$1</h2>')
            .replace(/^# (.+)$/gm, '<h1>$1</h1>')
            .replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>')
            .replace(/\*(.+?)\*/g, '<em>$1</em>')
            .replace(/^- (.+)$/gm, '<li>$1</li>')
            .replace(/(<li>.*<\/li>)/s, '<ul>$1</ul>')
            .replace(/\n{2,}/g, '</p><p>')
            .replace(/\n/g, '<br>');
    }

